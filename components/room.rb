class Room < Recs::Component
  attr_reader :room

  def initialize
    super
    map = <<~MAP
    ---------------  -------
    |.............|--|.....|
    |......................|
    |.............|--|.....|
    ---------------  --|.|--
                       |.|
                       |.|
                       |.|      -----------
                       |.|      |.........|
                       |.|      |.........|
                       |.|      |.........|
                       |.|      |.........|
                       |.|      |.........|
                       |.|      |..|-------
                       |.|------|..|
                       |...........|
                       |...........|
                       |......|-----
                       |......|
                       |......|
                       |......|
                       -------
    MAP
    @room = map.split("\n").map { |row| row.chars }
  end
end
